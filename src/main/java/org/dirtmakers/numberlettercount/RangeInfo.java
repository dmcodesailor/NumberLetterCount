package org.dirtmakers.numberlettercount;

/**
 *
 * @author brianlanham
 */
public class RangeInfo {
    private int fromVal;
    private int toVal;
    
    public int getFrom() {
        return this.fromVal;
    }
    
    public void setFrom(int fromVal) {
        this.fromVal = fromVal;
    }
    
    public int getTo() {
        return this.toVal;
    }
    
    public void setTo(int toVal) {
        this.toVal = toVal;
    }
}
